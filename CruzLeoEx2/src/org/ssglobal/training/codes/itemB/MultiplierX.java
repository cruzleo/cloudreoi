package org.ssglobal.training.codes.itemB;

public class MultiplierX {
	public void multiply() {
		// declare data
		double firstNum = 10;
		double secondNum = 20.5;
		double product = 0; 
		
		// calculate result
		product = firstNum * secondNum;
		
		// output result
		System.out.println(firstNum + " * " + secondNum + " = " + product);
	}
}
