package org.ssglobal.training.codes.itemB;

public class ArithmeticX {
	public void compute() {
		// declare variables
		float number1 = 1.1F;
		float number2 = 2.2F;
		float number3 = 3.3F;
		float average = 0;
		
		// calculate average
		average = number1 + number2 + number3 / 3;
		
		// print results
		System.out.println(number1 + ", " + number2 + " & " + number3 
				                   + " averages to " + average);
		
	}
}
